<h2>Programa Día Verde</h2>

<p>Descubrí tu día verde es un programa de recolección diferenciada en el cuál hay tres días para la recolección del residuo orgánico (bolsa negra) y un día, el DIA VERDE, para la bolsa verde.</p>

<p>La ciudad está dividida en seis zonas y a cada una le corresponde un día verde.</p>


<h2>Poda 2016</h2>

<p>Si necesitas podar, extraer o plantar algún árbol ubicado en la vereda del frente de tu casa, debes acercarte a nuestra oficina para solicitar autorización. Se llena un formulario y se arma una historia clínica del árbol para realizar un seguimiento de todas las acciones que se hagan en él.</p>

<p>Nos acercamos al domicilio a ver el ejemplar y determinar qué hacer con el árbol. Luego llamamos al vecino para informar lo resuelto.</p>


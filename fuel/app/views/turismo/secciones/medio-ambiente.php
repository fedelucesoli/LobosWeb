<p><strong>Adhesión del Municipio a la Red de Municipios frente al Cambio Climático (RAMCC)</strong></p>

<p>El intendente Jorge Etcheverry firmó un convenio de adhesión a la RAMCC en el cual se compromete a colaborar en la difusión de buenas prácticas ambientales y acciones ante el cambio climático para que la comunidad se convierta en agente multiplicador, realizar su Inventario de Gases de Efecto Invernadero, y desarrollar acciones y programas que contribuyan en la lucha frente al cambio climático.</p>

<p>&nbsp;</p>

<p><strong>Reacondicionamiento de la Planta de Tratamiento de Residuos Sólidos Urbanos</strong></p>

<p>Desde principio de año se comenzó con el reacondicionamiento de la PTRSU. Se hicieron arreglos en la infraestructura e instalaciones, se realizaron mejoras en cuanto a la logística de la misma, se entregó ropa y elementos de protección personal a los operarios. Actualmente, la planta está procesando el 100% de los residuos que llegan y se venden los reciclables. El dinero recaudado de éstos vuelve en infraestructura, mantenimiento, insumos y arreglos de la PTRSU.</p>

<p>&nbsp;</p>

<p><strong>Mesa de trabajo para armar el Plan de Arbolado Urbano</strong></p>

<p>Se realizan mesas de trabajo con viveristas, integrantes del CEA, espacios verdes y políticas ambientales en el que se comenzó a planificar el futuro plan de arbolado urbano.</p>

<p>Empezamos realizando un relevamiento del arbolado existente, para planificar la forestación por zonas.</p>


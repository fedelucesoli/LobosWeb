<h2>Observatorio del Deporte</h2>

<p>Una base de datos confiables que brinda información, conocimiento y datos específicos destinados a la toma de decisiones respecto a la salud, economía y a la política.</p>

<p>Respecto a la fecha el observatorio reúne y clasifica datos durante &nbsp;los 365 días del año.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<h2>Programa de promoción a los talentos deportivos</h2>

<p>Es un proceso sistemático, planeado a medio y largo plazo destinado a acceder a la excelencia a los deportes en competición. Considerando que constituye un factor esencial en el desarrollo deportivo de la Ciudad de Lobos, por el estímulo que suponen para el fomento del deporte de base y por su función representativa en la competiciones deportivas oficiales.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<h2>Deportes de las localidades rurales</h2>

<p>Programa destinado a llevar distintas propuestas deportivas tanto para las niñas como los niños de las distintas localidades rurales, brindando las mismas oportunidades que tienen los chicos de la planta urbana. A su vez se trabaja en afianzar un deporte especifico en cada localidad rural, ejemplo de ello es la Escuela Municipal de Canotaje. También se están organizando distintas competencias a realizarse en la zona rural de Lobos.</p></div>

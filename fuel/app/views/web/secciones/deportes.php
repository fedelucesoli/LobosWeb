<h2>Escuelas municipales de deportes</h2>

<p>Son Escuelas de iniciación deportiva que se desarrollan durante todo el año en los distintos barrio y localidades de Lobos, que tienen como eje principal la inclusión social y la educación de los niños y adolescentes a través de la educación física y el deporte.</p>

<p>Entre ellas se destaca la escuela Municipal de Natación Especial, la escuela Municipal de canotaje que funciona en el Club de Pesca, Escuela Municipal de handball próxima iniciarse en el Club Athletic, La Escuela Municipal de natación que funciona en el Club Las Delicias, la Escuela de Voley adaptado para adultos mayores que funciona en el C.E.F N°105 Lobos, Escuela Municipal de Remo próxima a iniciar sus actividades en la Laguna de Lobos.</p>


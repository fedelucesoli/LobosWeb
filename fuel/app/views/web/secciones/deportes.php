 <h2>Olimpiadas Cuenca del Salado</h2>

<p>Competencia multideportiva que se desarrolla en la ciudad de Lobos en la que participan 18 Municipios pertenecientes a la Cuenca del Salado con representantes en 41 disciplinas deportivas de proyección olímpica reuniendo en 2 fines de semana a más de 9000 personas en nuestra ciudad de Lobos.</p>


<p><strong>Historia, mapa turístico y servicios</strong></p>

<p>A tan sólo 100 km. de la Capital Federal y con una variada oferta turística, Lobos es una opción única para disfrutar con la familia y los amigos, todo el año.</p>

<p>El Partido de Lobos se caracteriza por la diversidad de atractivos que posee en toda la extensión de su territorio y una rica historia, que se puede ver reflejada en sus edificios y recorriendo sus calles.&nbsp;</p>


<h2>Programas</h2>

<p><strong>Niños Guías</strong></p>

<p>El proyecto “Niños guías” es diseñado para crear conciencia patrimonial en los niños de edad escolar del Partido de Lobos para contribuir al reconocimiento del lugar en el que habitan, para que luego, como adultos responsables, puedan comprometerse con la conservación del patrimonio arquitectónico y ambiental. Que nuestros niños sean idóneos y se pueda despertar en ellos el interés por la protección del patrimonio local. Que sean informantes y relatores de experiencias generando interés en demás actores locales.</p>

<p>No puede cuidarse lo que no se quiere, y no puede quererse aquello que no se conoce; es la intención de la Dirección de Turismo, que los niños conozcan “su lugar” y protejan el patrimonio tangible e intangible que hay en la localidad. Conceptos como “identidad” y “apropiación” serán utilizados para que los niños sientan el compromiso de cuidar lo nuestro.</p>

<p>&nbsp;</p>

<p><strong>Divertite en familia</strong></p>

<p>Viajar es una ocasión ideal para estrechar los lazos, es una excelente oportunidad para explorar el fascinante mundo que nos rodea mientras disfrutas de un tiempo de calidad. Sin duda, una oportunidad enriquecedora que ofrece experiencias de aprendizaje para todo el grupo familiar.</p>

<p>Lobos ofrece ámbitos naturales especiales para la familia, lugares para alojarse en contacto con la aturaleza y una amplia gastronomía.</p>

<p>Aprender, disfrutar, hacer deportes, diversión, todo en una sola ciudad y lo mejor de todo es poder hacerlo acompañado de la familia.</p>

<p>Realizar un paseo por la ciudad, visitar museos como el Museo de Perón y el Museo de Ciencias Naturales “Pago de los Lobos”, recrearse en el Parque Municipal Ingeniero Hiriart, Asistir a un espectáculo en el Teatro Italiano, Acampar en la Laguna, disfrutar de un paseo en bote o cabalgatas, visitar la feria de artesanías…y mucho más!</p>

<p>&nbsp;</p>

<p><strong>Disfrutá con amigos</strong></p>

<p>Desde el mismo instante en que se plantea la posibilidad de un viaje con amigos, se comienza un proyecto, se construye un sueño que cada día va tomando forma y el solo hecho de hacer planes para ese viaje los renueva, los motiva, les alegra el alma, los llena de expectativas. No sólo conocerán lugares maravillosos, sino que crearán&nbsp;vínculos que los acercarán&nbsp;aún más y vivirán&nbsp;experiencias que luego serán hermosas memorias.</p>

<p>Lobos está preparado para recibir al público joven, para brindarle la satisfacción de poder realizar aquellas actividades que no se hacen asiduamente, aquellas que despiertan los sentidos dormidos.</p>

<p>Lobos te propone realizar vuelos y saltos de Bautismo; pescar en la Laguna, practicar deportes acuáticos, visitar la ciudad, disfrutar de la variedad de bares, pubs y locales bailables o noches de fogón y guitarra… entre otras cosas!</p>

<p>&nbsp;</p>

<p><strong>Relajate en Pareja</strong></p>


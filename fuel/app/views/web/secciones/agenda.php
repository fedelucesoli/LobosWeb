<h3>Casa de la Historia y la Cultura del Bicentenario</h3><p>
	Espacio destinado al desarrollo de actividades culturales, educativas y recreativas y a la exposición permanente de diversos elementos que testimonien la historia, la actualidad y los proyectos culturales de nuestra comunidad.
	
	Allí funcionan los talleres de Arte Bidimensional y Tridimensional, Teatro, Danzas Libres, Fotografía y la Orquesta Escuela.</p>


<h3>Teatro Cine Italiano</h3>

<p>Espacio recuperado para la difusión de la Cultura. La sala funciona como cine, perteneciente a la red de Espacios INCAA. Se brindan funciones semanales jueves y domingos. La calidad en imagen y sonido es semejante a las mejores salas de Buenos Aires. También se ofrecen actividades especiales para el día del cine, vacaciones de invierno, día del niño, fechas patrias, etc.
	
	 
	
	La sala además funciona como Teatro ofreciendo obras de excelente nivel. Ya pasaron por este escenario: Claribel Medina, Nazarena Vélez, Hugo Varela, El Gato Peters, María Rosa Fugazot, Silvia Kutica, Viviana Saccone, Patricia Echegoyen, entre otros.
	
	 
	
	También el espacio es aprovechado para la difusión de artistas locales. Mensualmente se brindan espectáculos gratuitos con músicos y bailarines lobenses, mientras que en los pasillos se exponen los trabajos de artistas plásticos.
	
	 
	
	Por último el Teatro Cine Italiano es la sala de ensayo de la Orquesta Escuela, (conformada por más de cincuenta niños), del Coro Polifónico de Lobos y del Ballet Experimental Movimiento en Lobos.</p>


  <div class="pane-content">
    <p><strong>Ubicada a 100 km de&nbsp;Buenos Aires, Lobos&nbsp;es una ciudad de la&nbsp;provincia de Buenos Aires, Argentina, cabecera del&nbsp;partido homónimo. Traza su origen a la fundación realizada el&nbsp;2 de junio&nbsp;de&nbsp;1802&nbsp;por José Salgado.</strong></p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<h2>Marco histórico de su fundación</h2>

<p>&nbsp;</p>

<p>En el siglo XVIII ocurrieron dos procesos:</p>

<p>Las vaquerías indiscriminadas provocaron la disminución del ganado cimarrón, por lo que creció la importancia de las estancias como proveedoras de cueros para la exportación. En esta misma época los araucanos trasandinos, que ya habían empezado a cruzar la cordillera, predominando sobre los indios de la Patagonia y mezclándose con ellos avanzaron decididamente sobre la llanura y comenzaron a llevarse en gran escala arreos de ganados a Chile, disminuyendo la cantidad de vacunos en la pampa. El malón comenzó a generalizarse y con ello los enfrentamientos con los españoles, generándose así una lucha que solo acabaría varias décadas después de la Independencia, con la campaña de la Conquista del Desierto.</p>

<p>&nbsp;</p>

<p>La línea de fronteras pasó a primer plano en la preocupación de las autoridades y la ocupación de “la pampa” &nbsp;tomó características de conquista militar. En 1752 se crearon las famosas compañías de Blandengues: La Valerosa, que se instaló en las márgenes del Río Luján, La Invencible, que se acantonó en&nbsp;Salto (Buenos Aires)&nbsp;y La Atrevida Conquistadora que se ubicó en las márgenes del Río Samborombón, en el Zanjón, hoy Chascomús. Para su resguardo se construyeron tres fuertes, alrededor de los cuales se fue produciendo un espontáneo agrupamiento de habitantes.</p>

<p>&nbsp;</p>

<p>La línea de frontera fue consolidada por el Virrey Vértiz y su Comandante de Artillería de Fronteras Francisco de Betbezé, mediante la construcción o reconstrucción de fuertes en 1779, que fueron origen de importantes localidades bonaerenses, entre ellos el Fortín de San Pedro de Los Lobos.</p>

<p>&nbsp;</p>

<p>El mismo se erigió en la margen norte de la Laguna de Lobos, a trescientos metros de la ribera, en un lugar casi equidistante entre la desembocadura del Arroyo Las Garzas y el punto que nace el Saladillo Rodríguez y su obra se concluyó el 21 de agosto de 1779. El Fortín estaba formado por unos cuantos ranchos de barro y paja, sin puertas ni ventanas, rodeado por una defensa de palos a pique. La presencia de los indios se avisoraba desde el mangrullo. En 1792 lo habitaban 16 milicianos.</p>

<p>&nbsp;</p>

<p>Don José Salgado, que había recibido del Virrey, hacia fines del siglo XVIII, tierras para trabajar, erige a dos leguas al norte del Fortín y en el año 1802, una capilla a sus expensas y la dota de útiles y ornatos, celebrándose los primeros oficios religiosos el 9 de junio de 1803. La antigua capilla fue levantada en el solar que ocupa el actual Templo Parroquial.</p>

<p>Nace así, al crearse este curato perteneciente a la Parroquia de Morón, el pueblo actual, que no tenía más vecinos que la familia Salgado, ya que el resto de la población, compuesta por 141 familias se hallaba dispuesto en chacras y estancias en el resto del Distrito. Al instalarse la Capilla, algunas Familias comienzan a afincarse junto al camino de las carretas por lo que surge la necesidad de delimitar la traza del Pueblo.</p>

<p>&nbsp;</p>

<p>En 1804, don Ramón de Urquiola, vecino del lugar, apoyado por el Cura Párroco Don José García Miranda, solicita al Virrey la Traza del Pueblo de San Salvador de Los Lobos y la demarcación de un terreno para el ganado de la Iglesia, lográndose recién en el año 1811 la delimitación del Pueblo de San Salvador de la Guardia del Partido de Lobos.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<h2>Transporte</h2>

<p>&nbsp;</p>

<p><strong>Trenes</strong></p>

<p>Cuenta con la&nbsp;Estación de Trenes Lobos, que presta servicios interurbanos diarios hacia las ciudades de&nbsp;General Las Heras,&nbsp;Marcos Paz&nbsp;y&nbsp;Merlo&nbsp;a través de la&nbsp;Línea Sarmiento.</p>

<p>&nbsp;</p>

<p>Además a través de la&nbsp;Línea General Roca&nbsp;está comunicada con la&nbsp;Estación Plaza Constitución&nbsp;en la&nbsp;Ciudad de Buenos Aires&nbsp;y las ciudades bonaerenses de&nbsp;Saladillo&nbsp;y&nbsp;General Alvear.</p>

